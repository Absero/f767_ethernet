/**
 * @brief Function implementing the dhcpTask thread.
 * @param argument: Not used
 * @retval None
 */
/* USER CODE END Header_Start_DHCP_Task */
void Start_DHCP_Task(void const * argument)
{
  /* USER CODE BEGIN Start_DHCP_Task */
#if USE_DHCP
	struct netif *netif = (struct netif*) argument;
	ip_addr_t ipaddr;
	ip_addr_t netmask;
	ip_addr_t gw;
	struct dhcp *dhcp;

	/* Infinite loop */
	for (;;) {
//		HAL_GPIO_TogglePin(LD1_GPIO_Port, LD1_Pin);
//		osDelay(1000);
		switch (DHCP_state) {
		case DHCP_START :
			ip_addr_set_zero_ip4(&netif->ip_addr);
			ip_addr_set_zero_ip4(&netif->netmask);
			ip_addr_set_zero_ip4(&netif->gw);
			break;
		case DHCP_WAIT_ADDRESS :
			if (dhcp_supplied_address(netif))
				DHCP_state = DHCP_ADDRESS_ASSIGNED;
			else {
				dhcp = (struct dhcp*) netif_get_client_data(netif,
						LWIP_NETIF_CLIENT_DATA_INDEX_DHCP);

				/* DHCP timeout */
				if (dhcp->tries > MAX_DHCP_TRIES) {
					DHCP_state = DHCP_TIMEOUT;

					/* Stop DHCP */
					dhcp_stop(netif);

					/* Static address used */
					IP_ADDR4(&ipaddr, 192, 168, 1, 49);
					IP_ADDR4(&netmask, 255, 255, 255, 0);
					IP_ADDR4(&gw, 192, 168, 1, 1);
					netif_set_addr(netif, ip_2_ip4(&ipaddr), ip_2_ip4(&netmask),
							ip_2_ip4(&gw));
				}
			}
			break;
		case DHCP_LINK_DOWN :
			/* Stop DHCP */
			dhcp_stop(netif);
			DHCP_state = DHCP_OFF;
			break;
		default:
			break;
		}

		osDelay(250);
	}
#else
	for (;;) {
		osThreadTerminate(NULL);
	}
#endif
  /* USER CODE END Start_DHCP_Task */
}

